PRIORITY = [
    "地球科学", "旧日", "故土与家", "行记", "音乐", "美食", "校园", "思辨", "随笔",
]


def classify(text: str) -> tuple[str, list[str]]:
    """
    返回 (主分类, 标签列表)。
    关键词命中数多者胜出，平局按优先级取。
    无命中时返回 ("随笔", [])。
    """
    scores = {}
    tags = []

    for category, keywords in CATEGORY_KEYWORDS.items():
        score = 0
        for kw in keywords:
            if kw in text:
                score += 1
                tags.append(kw)
        if score > 0:
            scores[category] = score

    if not scores:
        return ("随笔", tags)

    # 取最高分，平局按优先级
    max_score = max(scores.values())
    candidates = [c for c, s in scores.items() if s == max_score]
    if len(candidates) == 1:
        return (candidates[0], tags)

    for cat in PRIORITY:
        if cat in candidates:
            return (cat, tags)

    return ("随笔", tags)


